void testcase() {
    int n; cin >> n;

    vector<int> a(n);
    for (int i = 0; i < n; i++) cin >> a[i];

    vector<int> b(2 * n);
    for (int i = 0; i < n; i++) {
        b[i] = b[i + n] = a[i];
    }

    vector<int> lis;
    int sz = 0;

    for (int s = 0; s < n; s++) {
        lis.clear();

        for (int i = s; i < s + n; i++) {
            int x = b[i];
            
            auto it = lower_bound(all(lis), x);
            
            if (it == lis.end()) lis.pb(x);
            else *it = x;
        }

        sz = max(sz, (int)lis.size());
    }

    cout << n - sz << el;
}

int32_t main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    file();

    int t = 1; //cin >> t;
    while (t--) testcase();

    return 0;
}
