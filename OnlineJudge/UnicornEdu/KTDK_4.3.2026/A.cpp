void testcase() {
	ll n; cin >> n;
	ll res = 0;
	ll m = 0;
	for (ll i = n / 2; i <= n - 1; i++) {
		if ( __gcd(i, n) + i >= res) {
			res = __gcd(i, n) + i;
			m = i;
		}
	}
	
	cout << m;
}

