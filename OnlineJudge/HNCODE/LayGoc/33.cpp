void testcase() {
	int n; cin >> n;
	ll cnt = 0;
	for (int i = 1; i <= n; i++) {
		for (int j = i + 1; j <= n; j++) {
			for (int k = j + 1; k <= n; k++) {
				for (int l = k + 1; l <= n; l++) {
					if (i + j + k + l == n) ++cnt;
				}
			}
		}
	}
	cout << cnt;
}


int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	//file();

	ll t = 1; //cin >> t;
	while (t--) testcase();

	return 0;
}
